
public class Loops {
    public static void main(String[] args) {
        //for loop
        for(int i=0; i<10; i++){
            System.out.println(i);
        }

        //while loop
        int j = 1;
        while (j <= 5) {
            System.out.println("Pakistan Zindabad!");
            j++;
        }

        //do-while loop
        int k = 1;
        do {
            System.out.println("Hello World!");
            k++;
        } while (k <= 5);
    }
}
